#include <iostream>
using namespace std;

int main() {
    int a,b,c,d,e;
    //한 줄의 수를 하나씩 저장하는 법
    scanf("%1d%1d%1d%1d%1d",&a,&b,&c,&d,&e);
    cout <<"["<<a*10000<<"]"<<"\n";
    cout <<"["<<b*1000<<"]"<<"\n";
    cout <<"["<<c*100<<"]"<<"\n";
    cout <<"["<<d*10<<"]"<<"\n";
    cout <<"["<<e<<"]"<<"\n";
}
